export async function runScenario({ assetId = "invoice-usdc-7d", requestedAmountUsd } = {}) {
  const dataset = await loadSignals();
  const asset = dataset.assets.find((item) => item.id === assetId) || dataset.assets[0];
  const run = {
    id: `run-${crypto.randomUUID()}`,
    startedAt: new Date().toISOString(),
    objective:
      "Autonomously screen an RWA allocation, pay for risk intelligence, and anchor a Casper audit receipt."
  };

  const riskRequirement = buildPaymentRequirement("rwa.risk_score", asset);
  const riskProof = signDemoPayment(riskRequirement, { agentId: AGENT_ID });
  const riskResult = await callPaidRiskOracle({
    assetId: asset.id,
    requestedAmountUsd: requestedAmountUsd || asset.requestedAmountUsd,
    paymentProof: riskProof
  });

  if (riskResult.status !== 200) {
    throw new Error(`Risk oracle failed: ${riskResult.body.error}`);
  }

  const kybRequirement = buildPaymentRequirement("rwa.kyb_screen", asset);
  const kybProof = signDemoPayment(kybRequirement, { agentId: AGENT_ID });
  const kybResult = await callPaidKybOracle({
    assetId: asset.id,
    paymentProof: kybProof
  });

  if (kybResult.status !== 200) {
    throw new Error(`KYB oracle failed: ${kybResult.body.error}`);
  }

  const liquidityRequirement = buildPaymentRequirement("rwa.liquidity_depth", asset);
  const liquidityProof = signDemoPayment(liquidityRequirement, { agentId: AGENT_ID });
  const liquidityResult = await callPaidLiquidityOracle({
    assetId: asset.id,
    requestedAmountUsd: requestedAmountUsd || asset.requestedAmountUsd,
    paymentProof: liquidityProof
  });

  if (liquidityResult.status !== 200) {
    throw new Error(`Liquidity oracle failed: ${liquidityResult.body.error}`);
  }

  const covenantRequirement = buildPaymentRequirement("rwa.covenant_monitor", asset);
  const covenantProof = signDemoPayment(covenantRequirement, { agentId: AGENT_ID });
  const covenantResult = await callPaidCovenantOracle({
    assetId: asset.id,
    requestedAmountUsd: requestedAmountUsd || asset.requestedAmountUsd,
    paymentProof: covenantProof
  });

  if (covenantResult.status !== 200) {
    throw new Error(`Covenant oracle failed: ${covenantResult.body.error}`);
  }

  const riskReport = riskResult.body.report;
  const kybReport = kybResult.body.report;
  const liquidityReport = liquidityResult.body.report;
  const covenantReport = covenantResult.body.report;
  const decision = decide(riskReport, kybReport, liquidityReport, covenantReport);
  const payments = [
    riskResult.body.payment,
    kybResult.body.payment,
    liquidityResult.body.payment,
    covenantResult.body.payment
  ];
  const receipt = buildReceipt({
    agentRun: run,
    payments,
    riskReport,
    kybReport,
    liquidityReport,
    covenantReport,
    decision
  });
  const anchor = await anchorReceipt(receipt);
  const providerRevenue = summarizeProviderRevenue(payments);

  const result = {
    run: {
      ...run,
      completedAt: new Date().toISOString()
    },
    trace: [
      {
        label: "Discover MCP tool",
        status: "complete",
        detail: "Agent found paid risk, KYB, liquidity, and covenant tools with x402-Casper payment requirements."
      },
      {
        label: "Sign x402-Casper payments",
        status: "complete",
        detail: `Ed25519 authorizations signed for ${payments.length} paid tool calls.`
      },
      {
        label: "Settle paid tools",
        status: "complete",
        detail: `${providerRevenue.totalCSPR} CSPR settled across risk, KYB, liquidity, and covenant providers.`,
        txHash: payments.map((item) => item.txHash).join(" | ")
      },
      {
        label: "Call paid risk oracle",
        status: "complete",
        detail: `${asset.name} scored ${riskReport.score}/100 with ${Math.round(riskReport.confidence * 100)}% confidence.`
      },
      {
        label: "Call paid KYB oracle",
        status: "complete",
        detail: `${kybReport.entityStatus} entity, sanctions ${kybReport.sanctionsScreen}, policy ${kybReport.policyResult}.`
      },
      {
        label: "Call paid liquidity oracle",
        status: "complete",
        detail: `${liquidityReport.coverageRatio}x coverage, ${liquidityReport.slippageBps} bps estimated slippage.`
      },
      {
        label: "Call paid covenant monitor",
        status: "complete",
        detail: `${covenantReport.policyResult} monitoring, ${covenantReport.reviewCadenceHours}h review cadence.`
      },
      {
        label: "Make allocation decision",
        status: "complete",
        detail: `${decision.action.toUpperCase()} ${formatUsd(decision.approvedAmountUsd)} at risk tier ${decision.riskTier}.`
      },
      {
        label: "Anchor Casper receipt",
        status: "complete",
        detail: `Receipt hash ${receipt.receiptHash.slice(0, 16)}... anchored.`,
        txHash: anchor.deployHash
      }
    ],
    paymentRequirements: [riskRequirement, kybRequirement, liquidityRequirement, covenantRequirement],
    payments,
    providerRevenue,
    riskReport,
    kybReport,
    liquidityReport,
    covenantReport,
    decision,
    anchor
  };

  result.ledgerEntry = await recordRun(result);
  result.evidenceBundle = await recordEvidenceBundle(result);
  return result;
}

export async function callPaidRiskOracle({ assetId, requestedAmountUsd, paymentProof }) {
  const dataset = await loadSignals();
  const asset = dataset.assets.find((item) => item.id === assetId);

  if (!asset) {
    return { status: 404, body: { error: "Unknown assetId" } };
  }

  if (!paymentProof) {
    const requirement = buildPaymentRequirement("rwa.risk_score", asset);
    return {
      status: 402,
      headers: paymentHeaders(requirement),
      body: {
        error: "Payment required",
        requirement
      }
    };
  }

  const verification = verifyPayment(asset, "rwa.risk_score", paymentProof);
  if (!verification.isValid) {
    const requirement = buildPaymentRequirement("rwa.risk_score", asset);
    return {
      status: 402,
      headers: paymentHeaders(requirement),
      body: {
        error: verification.invalidReason,
        requirement
      }
    };
  }

  return {
    status: 200,
    body: {
      report: scoreAsset(asset, requestedAmountUsd || asset.requestedAmountUsd),
      payment: verification.settlement
    }
  };
}

