export async function loadSignals() {
  return JSON.parse(await fs.readFile(DATA_PATH, "utf8"));
}

export function evaluatePolicyScenario(asset, requestedAmountUsd = asset.requestedAmountUsd) {
  const riskReport = scoreAsset(asset, requestedAmountUsd);
  const kybReport = buildKybReport(asset);
  const liquidityReport = buildLiquidityReport(asset, requestedAmountUsd);
  const covenantReport = buildCovenantReport(asset, requestedAmountUsd);
  const decision = decide(riskReport, kybReport, liquidityReport, covenantReport);

  return {
    asset,
    requestedAmountUsd,
    riskReport,
    kybReport,
    liquidityReport,
    covenantReport,
    decision
  };
}

function buildPaymentRequirement(tool, asset) {
  const resourceByTool = {
    "rwa.risk_score": "rwa-risk",
    "rwa.kyb_screen": "kyb-screen",
    "rwa.liquidity_depth": "liquidity-depth",
    "rwa.covenant_monitor": "covenant-monitor"
  };
  const descriptionByTool = {
    "rwa.risk_score": `Risk scoring for ${asset.name}`,
    "rwa.kyb_screen": `KYB and sanctions screening for ${asset.name}`,
    "rwa.liquidity_depth": `Liquidity depth and slippage analysis for ${asset.name}`,
    "rwa.covenant_monitor": `Post-allocation covenant monitoring for ${asset.name}`
  };

  return createPaymentRequirement({
    tool,
    assetId: asset.id,
    amount: TOOL_PRICES[tool],
    resource: `/api/oracle/${resourceByTool[tool]}?assetId=${asset.id}`,
    description: descriptionByTool[tool]
  });
}

function scoreAsset(asset, requestedAmountUsd) {
  const health = clamp(asset.counterpartyHealth / 100, 0, 1);
  const liquidity = clamp(asset.liquidityDepthUsd / Math.max(requestedAmountUsd * 5, 1), 0, 1);
  const compliance =
    asset.kybStatus === "verified" && asset.sanctionsScreen === "clear"
      ? 1
      : asset.sanctionsScreen === "clear"
        ? 0.62
        : 0.1;
  const repayment = clamp(1 - asset.latePaymentRate90d * 6, 0, 1);
  const stability = clamp(1 - asset.volatility30d * 8, 0, 1);
  const yieldQuality = clamp(asset.apr / 14, 0, 1);

  const rawScore =
    health * 0.23 +
    liquidity * 0.18 +
    compliance * 0.22 +
    repayment * 0.17 +
    stability * 0.12 +
    yieldQuality * 0.08;

  const score = Math.round(rawScore * 100);
  const maxExposureUsd = Math.round(requestedAmountUsd * clamp(rawScore - 0.08, 0.05, 0.86));

  return {
    asset,
    requestedAmountUsd,
    score,
    confidence: asset.oracleConfidence,
    maxExposureUsd,
    riskTier: score >= 85 ? "A" : score >= 72 ? "B" : score >= 58 ? "C" : "D",
    factors: [
      { label: "Counterparty health", value: Math.round(health * 100) },
      { label: "Liquidity coverage", value: Math.round(liquidity * 100) },
      { label: "Compliance clarity", value: Math.round(compliance * 100) },
      { label: "Repayment history", value: Math.round(repayment * 100) },
      { label: "Market stability", value: Math.round(stability * 100) },
      { label: "Yield quality", value: Math.round(yieldQuality * 100) }
    ],
    warnings: buildWarnings(asset, score)
  };
}

function buildKybReport(asset) {
  const jurisdictionRisk =
    {
      US: "low",
      EU: "low",
      BR: "medium",
      AE: "medium",
      SG: "low"
    }[asset.jurisdiction] || "medium";
  const entityStatus = asset.kybStatus === "verified" ? "verified" : "needs-review";
  const policyResult =
    entityStatus === "verified" && asset.sanctionsScreen === "clear" && jurisdictionRisk !== "high"
      ? "pass"
      : "manual-review";

  return {
    assetId: asset.id,
    entityStatus,
    sanctionsScreen: asset.sanctionsScreen,
    jurisdiction: asset.jurisdiction,
    jurisdictionRisk,
    policyResult,
    reviewedAt: new Date().toISOString(),
    flags: [
      ...(asset.kybStatus === "verified" ? [] : ["KYB document freshness needs review"]),
      ...(asset.sanctionsScreen === "clear" ? [] : ["Sanctions match requires escalation"]),
      ...(jurisdictionRisk === "low" ? [] : [`${asset.jurisdiction} jurisdiction risk is ${jurisdictionRisk}`])
    ]
  };
}

function buildLiquidityReport(asset, requestedAmountUsd) {
  const coverageRatio = asset.liquidityDepthUsd / Math.max(requestedAmountUsd, 1);
  const utilization = clamp(requestedAmountUsd / Math.max(asset.liquidityDepthUsd, 1), 0, 1);
  const slippageBps = Math.round((utilization ** 1.4) * 220 + asset.volatility30d * 1200);
  const exitWindowHours = coverageRatio >= 8 ? 2 : coverageRatio >= 4 ? 8 : coverageRatio >= 2 ? 24 : 72;
  const maxExecutableUsd = Math.round(asset.liquidityDepthUsd * 0.22);
  const liquidityGrade =
    coverageRatio >= 8 && slippageBps <= 65
      ? "A"
      : coverageRatio >= 4 && slippageBps <= 110
        ? "B"
        : coverageRatio >= 2
          ? "C"
          : "D";

  return {
    assetId: asset.id,
    liquidityDepthUsd: asset.liquidityDepthUsd,
    requestedAmountUsd,
    coverageRatio: Number(coverageRatio.toFixed(2)),
    utilization: Number(utilization.toFixed(3)),
    slippageBps,
    exitWindowHours,
    maxExecutableUsd,
    liquidityGrade,
    policyResult: liquidityGrade === "A" || liquidityGrade === "B" ? "pass" : "cap-or-review",
    warnings: [
      ...(coverageRatio < 3 ? ["Requested allocation is large relative to exit liquidity"] : []),
      ...(slippageBps > 120 ? ["Estimated slippage exceeds autonomous policy threshold"] : []),
      ...(exitWindowHours > 24 ? ["Exit window too long for autonomous treasury policy"] : [])
    ]
  };
}

function buildCovenantReport(asset, requestedAmountUsd) {
  const allocationRatio = requestedAmountUsd / Math.max(asset.liquidityDepthUsd, 1);
  const defaultRisk = clamp(asset.latePaymentRate90d * 8 + (100 - asset.counterpartyHealth) / 220, 0, 1);
  const volatilityRisk = clamp(asset.volatility30d * 9, 0, 1);
  const reviewCadenceHours = defaultRisk > 0.55 || volatilityRisk > 0.5 ? 12 : allocationRatio > 0.2 ? 24 : 72;
  const policyResult =
    defaultRisk > 0.72 || volatilityRisk > 0.72
      ? "critical-watch"
      : reviewCadenceHours <= 24
        ? "active-watch"
        : "standard-watch";

  return {
    assetId: asset.id,
    requestedAmountUsd,
    policyResult,
    reviewCadenceHours,
    defaultRisk: Number(defaultRisk.toFixed(3)),
    volatilityRisk: Number(volatilityRisk.toFixed(3)),
    triggers: [
      {
        metric: "latePaymentRate90d",
        current: asset.latePaymentRate90d,
        alertAbove: 0.05
      },
      {
        metric: "liquidityDepthUsd",
        current: asset.liquidityDepthUsd,
        alertBelow: Math.round(requestedAmountUsd * 3)
      },
      {
        metric: "volatility30d",
        current: asset.volatility30d,
        alertAbove: 0.045
      },
      {
        metric: "counterpartyHealth",
        current: asset.counterpartyHealth,
        alertBelow: 70
      }
    ],
    recommendedActions: [
      ...(policyResult === "critical-watch" ? ["Block autonomous execution until reviewer approval"] : []),
      ...(reviewCadenceHours <= 24 ? ["Enable daily covenant checks"] : ["Enable weekly covenant checks"]),
      "Persist monitoring proof with the Casper receipt"
    ]
  };
}

function verifyPayment(asset, tool, paymentProof) {
  return verifyAndSettlePayment(paymentProof, {
    tool,
    assetId: asset.id,
    minAmount: TOOL_PRICES[tool],
    payTo: process.env.CASPER_TREASURY_PUBLIC_KEY || "demo-casper-treasury"
  });
}

function paymentHeaders(requirement) {
  const encoded = encodePaymentHeader(requirement);
  return {
    "PAYMENT-REQUIRED": encoded,
    "X-PAYMENT-REQUIRED": encoded
  };
}

function summarizeProviderRevenue(payments) {
  const total = payments.reduce((sum, item) => sum + Number(item.amount), 0);
  return {
    totalCSPR: total.toFixed(2),
    providers: payments.map((item) => ({
      tool: item.tool,
      amount: item.amount,
      currency: item.currency,
      txHash: item.txHash
    }))
  };
}

function decide(report, kybReport, liquidityReport, covenantReport) {
  const compliancePass = kybReport.policyResult === "pass";
  const liquidityPass = liquidityReport.policyResult === "pass";
  const covenantPass = covenantReport.policyResult !== "critical-watch";
  const executableAmountUsd = Math.min(report.maxExposureUsd, liquidityReport.maxExecutableUsd);

  if (report.score >= 82 && report.confidence >= 0.88 && compliancePass && liquidityPass && covenantPass) {
    return {
      action: "approve",
      riskTier: report.riskTier,
      approvedAmountUsd: executableAmountUsd,
      humanReviewRequired: false,
      rationale:
        "Risk, KYB, liquidity, and covenant policies pass, so the agent can autonomously approve a capped allocation."
    };
  }

  if (report.score >= 68 && report.confidence >= 0.78 && compliancePass && covenantPass) {
    return {
      action: "limit",
      riskTier: report.riskTier,
      approvedAmountUsd: Math.round(executableAmountUsd * 0.55),
      humanReviewRequired: true,
      rationale:
        "The opportunity is viable, but liquidity or risk constraints require capped exposure and reviewer sign-off."
    };
  }

  return {
    action: "reject",
    riskTier: report.riskTier,
    approvedAmountUsd: 0,
    humanReviewRequired: true,
    rationale:
      "Risk-adjusted score or oracle confidence is below the autonomous policy threshold."
  };
}

function buildWarnings(asset, score) {
  const warnings = [];
  if (asset.kybStatus !== "verified") warnings.push("KYB requires manual review");
  if (asset.latePaymentRate90d > 0.05) warnings.push("Elevated 90-day late-payment rate");
  if (asset.liquidityDepthUsd < asset.requestedAmountUsd * 3) warnings.push("Thin exit liquidity");
  if (score < 68) warnings.push("Policy score below autonomous allocation threshold");
  return warnings;
}

function clamp(value, min, max) {
  return Math.min(Math.max(value, min), max);
}

function formatUsd(value) {
  return new Intl.NumberFormat("en-US", {
    style: "currency",
    currency: "USD",
    maximumFractionDigits: 0
  }).format(value);
}
